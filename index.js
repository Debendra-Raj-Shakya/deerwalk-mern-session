let mul =7;

for (let i=1;i<10;i++){
    // console.log(i*mul)
    result=i * mul
    console.log(`${i} *  ${mul} = ${result}`)
}
let j=0
while(j<10){
    result=j * mul
    console.log(`${j} *  ${mul} = ${result}`)
    j++;
}
let k=1
do{
    result=k * mul
    console.log(`${k} *  ${mul} = ${result}`)
    k++;
}while(k <= 10)